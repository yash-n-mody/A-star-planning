def ToWorldCoord(p, q):
    p = float(translate(p, 0, 17, -8.76, 8.64))
    q = float(translate(q, 0, 19, 9.45, -9.54))
    return p, q

def ToMapCoord(p, q):
    p = int(math.ceil(translate(p, -8.76, 8.64, 0, 17)))
    q = int(math.ceil(translate(q, 9.45, -9.54, 0, 19)))
    return p, q

def translate(sensor_val, in_from, in_to, out_from, out_to):
    out_range = out_to - out_from
    in_range = in_to - in_from
    in_val = sensor_val - in_from
    val=(float(in_val)/in_range)*out_range
    # print(val)
    out_val = out_from+val
    return out_val

